////////////////////////////////////////////////////////////////////////////////
int getu32 (uint8_t *p){

	uint32_t res = 0;

	res = p[3];
	res <<= 8;
	res |= p[2];
	res <<= 8;
	res |= p[1];
	res <<= 8;
	res |= p[0];

	return res;
}

////////////////////////////////////////////////////////////////////////////////
int getu16 (uint8_t *p){

	uint32_t res = 0;

	res = p[1];
	res <<= 8;
	res |= p[0];

	return res;
}

////////////////////////////////////////////////////////////////////////////////
void addbyte (FILE *f, int *col, uint8_t byte){

	
	if (*col > 15){

		*col = 0;
		fprintf(f,"\n.byte ");
	}

	fprintf(f,"0x%02x",byte);
	if (*col < 15)
	fprintf(f,",");
	(*col)++;
}

